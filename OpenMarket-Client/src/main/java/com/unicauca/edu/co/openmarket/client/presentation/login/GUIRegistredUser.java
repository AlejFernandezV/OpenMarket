    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String password = String.valueOf(txtPassword.getPassword());
        String confirmingPassword = String.valueOf(txtPasswordConfirmation.getPassword());
        boolean validator = false;
        enumRoleUser rol = null;
        
        if(txtUserRol.getSelectedItem().equals("Vendedor")){
            rol = rol.SELLER;
        }
        else{
            rol = rol.REGISTERED;
        }
        while(!validator){
            if(password.equals(confirmingPassword)){
                validator = true;
            }else{
                errorMessage("Las contrase単as no coinciden, verifique por favor","Verificar Contrase単a");
            }
        }
        try{
            User newUser = new User(txtUser.getText(),password,txtUserNames.getText(),rol);
            validator = userAccess.save(newUser);
            if(!validator){
                errorMessage("No se pudo realizar el registro","Registro de Usuario");
                this.txtPassword.setText("");
                this.txtPasswordConfirmation.setText("");
            }else{
                successMessage("Bienvenido!","Registro de Usuario");
                JFrame frRegistered = new GUIRegistred(productAccess);
                frRegistered.setVisible(true);
                dispose();
            }
        }catch(Exception e){
            System.out.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

