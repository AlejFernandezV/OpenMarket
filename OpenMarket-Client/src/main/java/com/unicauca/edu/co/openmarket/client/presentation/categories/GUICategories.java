    private void cleanControls() {
        txtIdC.setText("");
        txtNombreC.setText("");
    }
    
    private void addCategory() {
        
        Category category = new Category();
        this.contID++;
        String name = txtNombreC.getText().trim();
        
        category.setCategoryId(contID);
        category.setName(name);
        
        OMAddCategoryCommand comm= new OMAddCategoryCommand(category, categoryAccess);
        ominvoker.addCommand(comm);
        ominvoker.execute();

        if (comm.result()) {
            successMessage("Se grabó con éxito", "Atención");
            cleanControls();
            stateInitial();
        } else {
            successMessage("Error al grabar, lo siento mucho", "Atención");
        }
    }

    private void editCategory() {
        String id = txtIdC.getText().trim();
        if (id.equals("")) {
            successMessage("Debe buscar el producto a editar", "Atención");
            txtIdC.requestFocus();
            return;
        }
        Long categoryId = Long.parseLong(id);
        Category category = new Category();
        category.setName(txtNombreC.getText().trim());

        try {
            if (categoryAccess.editC(categoryId, category)) {
                successMessage("Se editó con éxito", "Atención");
                cleanControls();
                stateInitial();
            } else {
                successMessage("Error al editar, lo siento mucho", "Atención");
            }
        } catch (Exception ex) {
            Logger.getLogger(GUICategories.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
