    private void btnEliminarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCActionPerformed
        String id = txtIdC.getText().trim();
        if (id.equals("")) {
            successMessage("Debe buscar el producto a eliminar", "Atención");
            txtIdC.requestFocus();
            return;
        }
        Long categoryId = Long.parseLong(id);                
        if (confirmMessage("Está seguro que desea eliminar este producto?", "Confirmación") == JOptionPane.YES_NO_OPTION) {
            try {
                if (categoryAccess.deleteC(categoryId)) {
                    successMessage("Producto eliminado con éxito", "Atención");
                    stateInitial();
                    cleanControls();
                }
            } catch (Exception ex) {
                Logger.getLogger(GUICategories.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarCActionPerformed

